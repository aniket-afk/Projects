        private void fetchDataFromDatabase() {
            Connection conn = null;
            PreparedStatement pst = null;

            try {
                conn = DriverManager.getConnection(admin.getDbUrl(), admin.getDbUsername(), admin.getDbPassword());

                // Execute a query to fetch all staff members
                String query = "SELECT LabRequestId,PatientName,PatientContact,PatientEmail,Doctor,Type_Of_Test,Status_of_Test,Result FROM TestRequest WHERE Status_of_Test = ?";
                pst = conn.prepareStatement(query);

                pst.setString(1, "Awaiting Result");

                // Get the result set from the prepared statement
                ResultSet resultSet = pst.executeQuery();

                // Populate the table with data from the result set
                populateTable(resultSet);

                // Close the result set and statement
                resultSet.close();
                pst.close();
                conn.close();

            } catch (SQLException e) {
                e.printStackTrace();
            }
        }
        private void populateTable(ResultSet resultSet) {
            try {
                DefaultTableModel model = (DefaultTableModel) tblRequests.getModel();
                model.setRowCount(0);

                ResultSetMetaData metaData = resultSet.getMetaData();
                int columnCount = metaData.getColumnCount();

                String[] columnNames = new String[columnCount];
                for (int i = 1; i <= columnCount; i++) {
                    columnNames[i - 1] = metaData.getColumnName(i);
                }

                model.setColumnIdentifiers(columnNames);

                while (resultSet.next()) {

                    Object[] rowData = new Object[columnCount];
                    for (int i = 1; i <= columnCount; i++) {
                        rowData[i - 1] = resultSet.getObject(i);
                    }
                    model.addRow(rowData);
                }
            } catch (SQLException e) {
                e.printStackTrace();
            }
    }//GEN-LAST:event_btnViewActionPerformed


