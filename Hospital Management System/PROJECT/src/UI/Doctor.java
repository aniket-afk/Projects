    private void updatePatientDetails(String patientName) {
        try (Connection conn = DriverManager.getConnection(admin.getDbUrl(), admin.getDbUsername(), admin.getDbPassword());
             PreparedStatement pst = conn.prepareStatement(
                 "SELECT * FROM Patients WHERE name = ? AND doctor = ? ORDER BY created_at DESC LIMIT 1")) {
            pst.setString(1, patientName);
            pst.setString(2, username);
            try (ResultSet rs = pst.executeQuery()) {
                if (rs.next()) {
                    txtPatAge.setText(rs.getString("age"));
                    txtPatDob.setText(rs.getString("dob"));
                    txtPatGender.setText(rs.getString("gender"));
                    txtAddress.setText(rs.getString("address"));
                    txtContact.setText(rs.getString("contact"));
                    txtGmail.setText(rs.getString("email"));
                    txtpatBloodGroup.setText(rs.getString("bloodGroup"));
                    txtPatHistory.setText(rs.getString("medicalHistory"));
                    txtPatAllergy.setText(rs.getString("allergy"));
                    txtReason.setText(rs.getString("reasonToVisit"));
                    // Add more fields as required
                }
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
