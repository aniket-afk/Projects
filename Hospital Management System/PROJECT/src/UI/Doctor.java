    public void populatePatientList() {
        try (Connection conn = DriverManager.getConnection(admin.getDbUrl(), admin.getDbUsername(), admin.getDbPassword());
             PreparedStatement pst = conn.prepareStatement(
                 "SELECT name, MAX(created_at) AS latest_entry FROM Patients WHERE doctor = ? GROUP BY name ORDER BY latest_entry DESC;")) {
            pst.setString(1, username);
            try (ResultSet rs = pst.executeQuery()) {
                while (rs.next()) {
                    jPatName.addItem(rs.getString("name"));
                }
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

