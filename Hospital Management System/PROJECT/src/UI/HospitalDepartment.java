    private void insertDepartmentIntoDatabase(String departmentName) {
        Connection conn = null;
        PreparedStatement pst = null;

        try {
            conn = DriverManager.getConnection(admin.getDbUrl(), admin.getDbUsername(), admin.getDbPassword());
            String sql = "INSERT INTO Department (department) VALUES (?)";
            pst = conn.prepareStatement(sql);
            pst.setString(1, departmentName);

            int result = pst.executeUpdate();
            if (result > 0) {
                JOptionPane.showMessageDialog(this, "Department created successfully.");
            } else {
                JOptionPane.showMessageDialog(this, "Failed to create department.", "Database Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage(), "Database Error", JOptionPane.ERROR_MESSAGE);
        } finally {
            // Close resources
            try {
                if (pst != null) pst.close();
                if (conn != null) conn.close();
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }
    }

